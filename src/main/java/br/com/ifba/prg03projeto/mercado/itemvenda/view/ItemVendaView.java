    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    
        try {
        int selectedRow = tblItens.getSelectedRow();

        if (selectedRow == -1) {
            throw new RuntimeException(
                    "Selecione um item na tabela."
            );
        }

        Long id = ((Number) tblItens.getValueAt(
                selectedRow,
                0
        )).longValue();

        ItemVenda item = itemVendaController.findById(id)
                .orElseThrow(() ->
                        new RuntimeException(
                                "Item da venda não encontrado."
                        )
                );

        itemSelecionadoId = item.getId();

        cboProduto.setSelectedItem(item.getProduto());
        cboProduto.setEnabled(false);

        txtQuantidade.setText(
                item.getQuantidade().toString()
        );

        txtPrecoUnitario.setText(
                String.format("%.2f", item.getPrecoUnitario())
        );

        txtSubtotal.setText(
                String.format("%.2f", item.getSubtotal())
        );
        
        btnAdicionar.setText("SALVAR");

    } catch (RuntimeException exception) {
        JOptionPane.showMessageDialog(
                this,
                exception.getMessage(),
                "Erro",
                JOptionPane.ERROR_MESSAGE
        );
    }
    }//GEN-LAST:event_btnEditarActionPerformed


