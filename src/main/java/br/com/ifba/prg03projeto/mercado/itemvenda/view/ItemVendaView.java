    private void loadProdutos() {
        cboProduto.removeAllItems();

        List<Produto> produtos = produtoController.findAll();

        for (Produto produto : produtos) {
            cboProduto.addItem(produto);
        }
    }

    private void loadTable() {
        DefaultTableModel model =
                (DefaultTableModel) tblItens.getModel();

        model.setRowCount(0);

        List<ItemVenda> itens =
                itemVendaController.findByVendaId(vendaId);

        for (ItemVenda item : itens) {
            model.addRow(new Object[]{
                item.getId(),
                item.getProduto().getNome(),
                item.getQuantidade(),
                String.format("%.2f", item.getPrecoUnitario()),
                String.format("%.2f", item.getSubtotal())
            });
        }
    }

    private void clearForm() {
        
    itemSelecionadoId = null;

    txtQuantidade.setText("");
    txtSubtotal.setText("");

    cboProduto.setEnabled(true);
    tblItens.clearSelection();

    if (cboProduto.getItemCount() > 0) {
        cboProduto.setSelectedIndex(0);

        Produto produto =
                (Produto) cboProduto.getSelectedItem();

        if (produto != null) {
            txtPrecoUnitario.setText(
                    String.format("%.2f", produto.getPreco())
            );
        }
    } else {
        txtPrecoUnitario.setText("");
    }
    btnAdicionar.setText("ADICIONAR");
        
    }

    private void calcularSubtotalVisual() {
        try {
            Produto produto =
                    (Produto) cboProduto.getSelectedItem();

            if (produto == null) {
                txtPrecoUnitario.setText("");
                txtSubtotal.setText("");
                return;
            }

            txtPrecoUnitario.setText(
                    String.format("%.2f", produto.getPreco())
            );

            Integer quantidade =
                    Integer.valueOf(txtQuantidade.getText());

            double subtotal =
                    produto.getPreco() * quantidade;

            txtSubtotal.setText(
                    String.format("%.2f", subtotal)
            );

        } catch (NumberFormatException exception) {
            txtSubtotal.setText("");
        }
    }
    
    

