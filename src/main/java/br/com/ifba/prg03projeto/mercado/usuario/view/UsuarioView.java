    private void carregarTabela() {

    javax.swing.table.DefaultTableModel modelo =
            (javax.swing.table.DefaultTableModel) tblUsuarios.getModel();

    modelo.setRowCount(0);

    java.util.List<br.com.ifba.prg03projeto.mercado.usuario.entity.Usuario> usuarios =
            usuarioController.findAll();

    for (br.com.ifba.prg03projeto.mercado.usuario.entity.Usuario usuario : usuarios) {

        modelo.addRow(new Object[]{
            usuario.getId(),
            usuario.getNome(),
            usuario.getLogin(),
            usuario.getPerfil()
        });
    }
    }
    
    private void limparCampos() {

    txtNome.setText("");
    txtLogin.setText("");
    txtSenha.setText("");

    if (cboPerfil.getItemCount() > 0) {
        cboPerfil.setSelectedIndex(0);
    }

    usuarioEdicao = null;

    txtNome.requestFocus();
    }
    
    
