    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    
        try {

        String nome = txtNome.getText().trim();
        String login = txtLogin.getText().trim();
        String senha = new String(txtSenha.getPassword());
        String perfil = cboPerfil.getSelectedItem().toString();

        br.com.ifba.prg03projeto.mercado.usuario.entity.Usuario usuario;

        if (usuarioEdicao == null) {
            usuario =
                    new br.com.ifba.prg03projeto.mercado.usuario.entity.Usuario();
        } else {
            usuario = usuarioEdicao;
        }

        usuario.setNome(nome);
        usuario.setLogin(login);
        usuario.setSenha(senha);
        usuario.setPerfil(perfil);

        if (usuarioEdicao == null) {
            usuarioController.save(usuario);
        } else {
            usuarioController.update(
                    usuarioEdicao.getId(),
                    usuario
            );
        }

        carregarTabela();
        limparCampos();

        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Usuário salvo com sucesso!",
                "Sucesso",
                javax.swing.JOptionPane.INFORMATION_MESSAGE
        );

    } catch (Exception e) {

        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Erro ao salvar usuário: " + e.getMessage(),
                "Erro",
                javax.swing.JOptionPane.ERROR_MESSAGE
        );
    }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
    
         int linha = tblUsuarios.getSelectedRow();

    if (linha >= 0) {

        Long id = Long.parseLong(
                tblUsuarios.getValueAt(linha, 0).toString()
        );

        br.com.ifba.prg03projeto.mercado.usuario.entity.Usuario usuario =
                usuarioController.findById(id).orElse(null);

        if (usuario != null) {

            usuarioEdicao = usuario;

            txtNome.setText(usuario.getNome());
            txtLogin.setText(usuario.getLogin());
            txtSenha.setText(usuario.getSenha());
            cboPerfil.setSelectedItem(usuario.getPerfil());

            txtNome.requestFocus();

        } else {

            javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Usuário não encontrado!",
                    "Erro",
                    javax.swing.JOptionPane.ERROR_MESSAGE
            );
        }

    } else {

        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Selecione um usuário para editar!",
                "Aviso",
                javax.swing.JOptionPane.WARNING_MESSAGE
        );
    }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
    
        int linha = tblUsuarios.getSelectedRow();

    if (linha >= 0) {

        int confirmacao = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Tem certeza que deseja remover este usuário?",
                "Confirmação",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirmacao == javax.swing.JOptionPane.YES_OPTION) {

            Long id = Long.parseLong(
                    tblUsuarios.getValueAt(linha, 0).toString()
            );

            try {

                usuarioController.delete(id);

                carregarTabela();
                limparCampos();

                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "Usuário removido com sucesso!",
                        "Sucesso",
                        javax.swing.JOptionPane.INFORMATION_MESSAGE
                );

            } catch (Exception e) {

                javax.swing.JOptionPane.showMessageDialog(
                        this,
                        "Erro ao remover usuário: " + e.getMessage(),
                        "Erro",
                        javax.swing.JOptionPane.ERROR_MESSAGE
                );
            }
        }

    } else {

        javax.swing.JOptionPane.showMessageDialog(
                this,
                "Selecione um usuário para remover!",
                "Aviso",
                javax.swing.JOptionPane.WARNING_MESSAGE
        );
    }
    }//GEN-LAST:event_btnRemoverActionPerformed

